export const CRITERIA_WEIGHTS = {
  location: 0.15,
  fieldDynamics: 0.15,
  parkStructure: 0.15,
  atmosphere: 0.15,
  historicIntegration: 0.10,
  foodMerch: 0.05,
  overallExperience: 0.25,
};

export function calculateTotalScore(scores: BallparkCriteria): number {
  const weighted =
    scores.location * CRITERIA_WEIGHTS.location +
    scores.fieldDynamics * CRITERIA_WEIGHTS.fieldDynamics +
    scores.parkStructure * CRITERIA_WEIGHTS.parkStructure +
    scores.atmosphere * CRITERIA_WEIGHTS.atmosphere +
    scores.historicIntegration * CRITERIA_WEIGHTS.historicIntegration +
    scores.foodMerch * CRITERIA_WEIGHTS.foodMerch +
    scores.overallExperience * CRITERIA_WEIGHTS.overallExperience +
    scores.tdfBonus * 0.1;
  return Math.round(weighted * 100) / 100;
}

export function getTier(score: number): { label: string; color: string } {
  if (score >= 8.5) return { label: "ELITE", color: "text-yellow-400 bg-yellow-400/10 border-yellow-400/30" };
  if (score >= 7.5) return { label: "Upper Tier", color: "text-green-400 bg-green-400/10 border-green-400/30" };
  if (score >= 6.5) return { label: "Average", color: "text-blue-400 bg-blue-400/10 border-blue-400/30" };
  if (score >= 5.5) return { label: "Lower Tier", color: "text-orange-400 bg-orange-400/10 border-orange-400/30" };
  return { label: "50 Feet of Crap", color: "text-red-400 bg-red-400/10 border-red-400/30" };
}
